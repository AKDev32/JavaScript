console.log(Math.abs(-10)); // 10
// Math.abs() is a built-in function that returns the absolute value of a number
console.log(Math.abs(10)); // 10
// It can also be used with variables
console.log(Math.floor(5.7)); // 5
// Math.floor() rounds down to the nearest integer
console.log(Math.ceil(5.1)); // 6
// Math.ceil() rounds up to the nearest integer
console.log(Math.round(5.5)); // 6
// Math.round() rounds to the nearest integer, rounding up if the decimal is .5 or higher
console.log(Math.max(1, 2, 3)); // 3
// Math.max() returns the largest of the given numbers
console.log(Math.min(1, 2, 3)); // 1
// Math.min() returns the smallest of the given numbers
console.log(Math.random()); // Random number between 0 and 1
// Math.random() generates a random floating-point number between 0 (inclusive) and 1 (exclusive)
console.log(Math.sqrt(16)); // 4
// Math.sqrt() returns the square root of a number
